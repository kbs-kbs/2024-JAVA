package ch04.sec02;

public class IfElseExample {

	public static void main(String[] args) {
		int score = 85;
		
		if (score >= 90) {
			System.out.println("Bigger than 90.");
			System.out.println("Your Grade is A.");
		} else {
			System.out.println("Smaller than 90.");
			System.out.println("Your Grade is B.");
		}

	}

}
